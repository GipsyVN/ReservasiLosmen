    private void cbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbActionPerformed
        String id = txtid.getText();
        JCheckBox cb = (JCheckBox) evt.getSource();
        if(cb.isSelected()){
            txtuname.setEnabled(true);
            txtpass.setEnabled(true);
            btnubah.setEnabled(true);
            
            try {
                koneksi.dbkoneksi();
                String sql= "Select * from login where ID = '"+id+"'";
                Statement pst = con.prepareStatement(sql); 
                ResultSet rs = pst.executeQuery(sql);
                if(rs.next()){    
                    txtuname.setText(rs.getString(2));
                    txtpass .setText(rs.getString(3));
                }
                con.close();
                pst.close();
            }catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
        }
            
        }else{
            txtuname.setEnabled(false);
            txtpass.setEnabled(false);
            btnubah.setEnabled(false);
            txtuname.setText("");
            txtpass.setText("");
        }
    }//GEN-LAST:event_cbActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel(); 
        int selectedRowIndex = jTable1.getSelectedRow();
        String id = (String) dtm.getValueAt(selectedRowIndex, 0);
        try {
            koneksi.dbkoneksi();
            String sql= "Select * from data_karyawan where ID_Pegawai= '"+id+"'";
            Statement pst = con.prepareStatement(sql); 
            ResultSet rs = pst.executeQuery(sql);
            if(rs.next()){    
                txtid.setText(rs.getString(1));
                txtnama.setText(rs.getString(2));
                cbjk.setSelectedItem(rs.getString(3));
                txtno.setText(rs.getString(4));
                txtalamat.setText(rs.getString(5));
                cbjabatan.setSelectedItem(rs.getString(6));
            }
            con.close();
            pst.close();
        }catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jTable1MouseClicked

