    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        String id = txtid.getText();
        String uname = txtuname.getText();
        String pass = txtpass.getText();
        String level = (String) cbjabatan.getSelectedItem();
        if(uname.isEmpty()){
            JOptionPane.showMessageDialog(null, "Username Tidak Boleh Kosong..");
            txtuname.requestFocus();
        }else if(pass.isEmpty()){
            JOptionPane.showMessageDialog(null, "Password Tidak Boleh Kosong..");
            txtpass.requestFocus();
        }else{
        int opsi = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin mengubah?", "Ubah Data", JOptionPane.YES_NO_OPTION);
        if(opsi == JOptionPane.YES_OPTION){
            try {
                koneksi.dbkoneksi();
                String sql= "update login set User_Name = '" + uname + "', Password = '" + pass + "', Level = '"+level+"' where ID= '"+id+"'";
                Statement pst = con.prepareStatement(sql); 
                pst.executeUpdate(sql);
                tabel();
                JOptionPane.showMessageDialog(null, "Berhasil...");
                con.close();
                pst.close();
            }catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
        }
    }//GEN-LAST:event_btnubahActionPerformed

