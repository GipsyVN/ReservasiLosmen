    private void tabel(){
        DefaultTableModel tb = new DefaultTableModel()
        {
        public boolean isCellEditable(int row, int column){
            return false;
        }
        };
        jTable1.setModel(tb);
        tb.addColumn("ID Pegawai");
        tb.addColumn("Nama");
        tb.addColumn("Jenis Kelamin");
        tb.addColumn("No HP");
        tb.addColumn("Alamat");
        tb.addColumn("Jabatan");
        
        try{
            koneksi.dbkoneksi();
            String sql = "select * from data_karyawan";
            ResultSet rs = stm.executeQuery(sql);
            while(rs.next()){
                tb.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                });
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        txtid.setText("");
        txtnama.setText("");
        cbjk.setSelectedItem("PILIH");
        txtno.setText("");
        txtalamat.setText("");
        cbjabatan.setSelectedItem("PILIH");
        txtpass.setText("");
        txtuname.setText("");
        cb.setSelected(false);
        txtuname.setEnabled(false);
        txtpass.setEnabled(false);
        btnubah.setEnabled(false);
        txtuname.setText("");
        txtpass.setText("");
        txtcari.setText("");
    }
    
    private void cari(String key){
        if(key.isEmpty()){
            JOptionPane.showMessageDialog(null, "Tidak Ada Data yang Dicari...");
            tabel();
        }else{
        try{
            DefaultTableModel tb = new DefaultTableModel()
        {
        public boolean isCellEditable(int row, int column){
            return false;
        }
        };
            jTable1.setModel(tb);
            tb.addColumn("ID Pegawai");
            tb.addColumn("Nama");
            tb.addColumn("Jenis Kelamin");
            tb.addColumn("No HP");
            tb.addColumn("Alamat");
            tb.addColumn("Jabatan");
            koneksi.dbkoneksi();
            String sql = "select * from data_karyawan where ID_Pegawai like '%"+key+"%' or Nama like '%"+key+"%' or Jenis_Kelamin like '%"+key+"%' or No_Hp like '%"+key+"%' or Alamat like '%"+key+"%' or Jabatan like '%"+key+"%'";
            ResultSet rs = stm.executeQuery(sql);
            while(rs.next()){
                Object[] data ={
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6)
                };
                tb.addRow(data);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        }
    }
