    private void tabel(){
        DefaultTableModel tb = new DefaultTableModel()
        {
        public boolean isCellEditable(int row, int column){
            return false;
        }
        };
        jTable1.setModel(tb);
        tb.addColumn("ID Tamu");
        tb.addColumn("Nama");
        tb.addColumn("Jenis Kelamin");
        tb.addColumn("No HP");
        tb.addColumn("Jenis Kamar");
        tb.addColumn("No Kamar");
        tb.addColumn("Durasi");
        tb.addColumn("Tanggal Check In");
        
        try{
            koneksi.dbkoneksi();
            String sql = "select * from tamu";
            ResultSet rs = stm.executeQuery(sql);
            while(rs.next()){
                tb.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getString(7),
                    rs.getString(8)
                });
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        lblid.setText("");
        lblnama.setText("");
        lbljenis.setText("");
        lblnh.setText("");
        lbljk.setText("");
        lblno.setText("");
        lbldurasi.setText("");
        lbltanggal.setText("");
        txtcari.setText("");
    }
    
    private void cari(String key){
        if(key.isEmpty()){
            JOptionPane.showMessageDialog(null, "Tidak Ada Data yang Dicari...");
            tabel();
        }else{
        try{
            DefaultTableModel tb = new DefaultTableModel();
            jTable1.setModel(tb);
            tb.addColumn("ID Tamu");
            tb.addColumn("Nama");
            tb.addColumn("Jenis Kelamin");
            tb.addColumn("No HP");
            tb.addColumn("Jenis Kamar");
            tb.addColumn("No Kamar");
            tb.addColumn("Durasi");
            tb.addColumn("Tanggal Check In");
            koneksi.dbkoneksi();
            String sql = "select * from tamu where ID_Tamu like '%"+key+"%' or Nama like '%"+key+"%' or Jenis_Kelamin like '%"+key+"%' or No_Hp like '%"+key+"%' or Jenis_Kamar like '%"+key+"%' or No_Kamar like '%"+key+"%' or Durasi like '%"+key+"%' or Tanggal_CheckIn like '%"+key+"%'";
            ResultSet rs = stm.executeQuery(sql);
            while(rs.next()){
                Object[] data ={
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getString(7),
                    rs.getString(8)
                };
                tb.addRow(data);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        }
    }
