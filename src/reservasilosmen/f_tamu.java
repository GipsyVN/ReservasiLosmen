    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel(); 
        int selectedRowIndex = jTable1.getSelectedRow();
        String id = (String) dtm.getValueAt(selectedRowIndex, 0);
        try {
            koneksi.dbkoneksi();
            String sql= "Select * from tamu where ID_Tamu = '"+id+"'";
            Statement pst = con.prepareStatement(sql); 
            ResultSet rs = pst.executeQuery(sql);
            if(rs.next()){    
                lblid.setText(rs.getString(1));
                lblnama.setText(rs.getString(2));
                lbljenis.setText(rs.getString(5));
                lblnh.setText(rs.getString(4));
                lbljk.setText(rs.getString(3));
                lblno.setText(rs.getString(6));
                lbldurasi.setText(rs.getString(7));
                lbltanggal.setText(rs.getString(8));
            }
            con.close();
            pst.close();
        }catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jTable1MouseClicked

